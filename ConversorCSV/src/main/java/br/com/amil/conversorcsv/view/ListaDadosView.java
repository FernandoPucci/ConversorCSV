    private void iniciaTabela() {

        jTableTabelaDados = new JTable(modelo);

        for (String s : colunas) {

            modelo.addColumn(s);

        }

        for (int i = 0; i < colunas.length; i++) {
            jTableTabelaDados.getColumnModel().getColumn(i).setPreferredWidth(1400);
        }

        modelo.setNumRows(0);

        for (Relatorio r : relatorios) {

            modelo.addRow(new Object[]{r.getCodigoPrestador(),
                r.getNomeOperadora(),
                r.getCodigoFilial(),
                r.getNomeFilial(),
                r.getRegiaoSaude(),
                r.getSubRegiaoSaude(),
                r.getCodigoUnidade(),
                r.getNomeUnidade(),
                r.getCodigoRede(),
                r.getNomeRede(),
                r.getTipoPessoa(),
                r.getCapituloOrientador(),
                r.getElementoDivulgacao(),
                r.getNomePrestador(),
                r.getTipoLogradouro(),
                r.getEnderecoPrestador(),
                r.getNumero(),
                r.getComplemento(),
                r.getMunicipio(),
                r.getBairro(),
                r.getCep(),
                r.getUf(),
                r.getDddTelefone1(),
                r.getTelefone1(),
                r.getDddTelefone2(),
                r.getTelefone2(),
                r.getDddFax(),
                r.getFax(),
                r.getDireciona(),
                r.getEmail(),
                r.getTipoEquipamento(),
                r.getIsComercializado(),
                r.getModeloOrientador(),
                r.getRedePropria(),
                r.getClassificacoes(), r.getA(), r.getB(), r.getC()
            });

        }

    }

